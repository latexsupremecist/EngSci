import numpy as np

a = np.array([1.5, 1.7, 2, 2.2, 2.5, 2.7, 3, 4, 5, 10, 15, 20, 25, 30])
a *= 0.01
V = np.array([0.000965, 0.000908, 0.000825, 0.000730, 0.000647, 0.000598, 0.000566, 0.000482, 0.000452, 0.000444, 0.000519, 0.000619, 0.000730, 0.000881])
I = np.array([1, 1, 1, 1, 1, 1, 1, 1, 0.95, 0.95, 0.95, 0.95, 0.95, 0.95])
aerr = 0.1 * 0.01
Verr = 0.5 * 10e-6
Ierr = 0.025

rho = 2 * np.pi * a * V / I
rhoerr = 2 * np.pi * np.sqrt((V * aerr / I) ** 2 + (a * Verr / I) ** 2 + (a * V * Ierr / (I ** 2)) ** 2)

for i in range(len(a)):
    print(a[i], aerr, rho[i], rhoerr[i])
